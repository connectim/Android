package connect.activity.wallet.manager;

public class PinBean {

    String baseSeed;
    String pin;

    public String getBaseSeed() {
        return baseSeed;
    }

    public void setBaseSeed(String baseSeed) {
        this.baseSeed = baseSeed;
    }

    public String getPin() {
        return pin;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }
}
